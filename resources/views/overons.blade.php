        <h1>Ruilhandel zonder geld? Hoe gaat dat?</h1>
        We ruilen onderling onze diensten en goederen. Nancy doet de boekhouding voor Mark. Mark verft het plafond voor Robin. Robin poetst het huis voor Petra. Petra verstelt een broek voor Yvonne. En Yvonne geeft een massage aan Nancy. Het bijzondere van een ruilkring is dat je voor de diensten en goederen zonder geld betaalt. We betalen onze diensten en goederen met een eigen ruilmiddel. In 's-Hertogenbosch en omstreken is dat de Niks. Dat is geen tastbaar voorwerp of een munt, maar een administratieve manier om de waarde van diensten of goederen vast te leggen. We ruilen dus met Niksen!

